-- ASC default order
SELECT * FROM CUSTOMERS
   ORDER BY NAME, SALARY;

-- DESC order
SELECT * FROM CUSTOMERS
    ORDER BY NAME DESC, SALARY DESC;


SELECT * FROM CUSTOMERS
   ORDER BY (CASE ADDRESS
   WHEN 'DELHI' 	 THEN 1
   WHEN 'BHOPAL' 	 THEN 2
   WHEN 'KOTA' 	 THEN 3
   WHEN 'AHMEDABAD' THEN 4
   WHEN 'MP' 	THEN 5
   ELSE 100 END) ASC, ADDRESS DESC;